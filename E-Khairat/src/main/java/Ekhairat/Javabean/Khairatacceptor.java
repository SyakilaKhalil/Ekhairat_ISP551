package Ekhairat.Javabean;

public class Khairatacceptor {
	int acceptorvalue;
	String memberid;
	String staffid;
	String acceptornote;
	
	public String getacceptornote() {
		return acceptornote;
	}
	public void setacceptornote(String acceptornote) {
		this.acceptornote = acceptornote;
	}
	public int getacceptorvalue() {
		return acceptorvalue;
	}
	public void setacceptorvalue(int acceptorvalue) {
		this.acceptorvalue = acceptorvalue;
	}
	public String getmemberid() {
		return memberid;
	}
	public void setmemberid(String memberid) {
		this.memberid = memberid;
	}
	public String getstaffid() {
		return staffid;
	}
	public void setstaffid(String staffid) {
		this.staffid = staffid;
	}
}
